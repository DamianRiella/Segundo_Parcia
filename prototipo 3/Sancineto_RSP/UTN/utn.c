/**
 * \brief Solicita un numero entero al usuario y lo valida
 * \param requestMessage Es el mensaje a ser mostrado para solicitar el dato
 * \param requestMessage Es el mensaje a ser mostrado en caso de error
 * \return El número ingresado por el usuario
 *
 */
int utn_getValidInt(char requestMessage[],char errorMessage[], int lowLimit, int hiLimit, int len)
{
    char auxStr[256];
    int auxInt;
    while(1)
    {
        if (!utn_getStringNumeros(requestMessage,auxStr, len))
        {
            printf ("%s\n",errorMessage);
            continue;

        }
        auxInt = atoi(auxStr);
        if(auxInt < lowLimit || auxInt > hiLimit)
        {
            printf ("El numero del debe ser mayor a %d y menor a %d\n",lowLimit,hiLimit);
            continue;

        }
        return auxInt;

    }


}

/**
 * \brief Limpia el stdin, similar a fflush
 * \param -
 * \return -
 *
 */
void utn_cleanStdin(void)
{
    int c;
    do {
        c = getchar();
    } while (c != '\n' && c != EOF);
}

/**
 * \brief Solicita un string
 * \param requestMessage Es el mensaje a ser mostrado para solicitar el dato
 * \param requestMessage Es el mensaje a ser mostrado en caso de error
 * \param input Array donde se cargará el texto ingresado
 * \return -
 *
 */
void utn_getValidString(char requestMessage[],char errorMessage[], char input[], int len)
{

    while(1)
    {
        if (!utn_getStringLetras(requestMessage,input, len))
        {
            printf ("%s\n",errorMessage);
            continue;
        }
        utn_cleanStdin();
        break;
    }

}
